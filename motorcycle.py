class motorcycle:
    def __init__(self,color,engineSize,manufacturer,model,year):
        self.color=color
        self.engineSize=engineSize
        self.manufacturer=manufacturer
        self.model=model
        self.year=year

    def getColor(self):
        return self.color 
    def getEngineSize(self):
        return self.engineSize
    def getManufacturer(self):
        return self.manufacturer 
    def getModel(self):
        return self.model 
    def getYear(self):
        return self.year 
